@pytest.mark.asyncio
async def test_unauthenticated_cannot_list_print_records(
    client: AsyncClient, db_session: AsyncSession
) -> None:
    """An unauthenticated request to the print-records list returns 401."""
    csrf = await _setup_admin(client)
    _, key = await _create_item(client, csrf, db_session)

    await client.post(
        f"/api/items/{key}/print-records",
        json={"note": "Private note", "visibility": "private"},
        headers={"X-CSRF-Token": csrf},
    )

    # Log out
    await client.post("/api/auth/logout", headers={"X-CSRF-Token": csrf})

    # Try to list — should get 401
    resp = await client.get(f"/api/items/{key}/print-records")
    assert resp.status_code in (401, 403), (
        f"Expected 401/403 but got {resp.status_code}: {resp.text}"
    )


