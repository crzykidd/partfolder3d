class TestGcodeTimeParser:
    """Test _parse_time_str with various time formats."""

    def test_hours_minutes_seconds(self) -> None:
        assert _parse_time_str("2h 3m 45s") == 7425

    def test_hours_minutes_only(self) -> None:
        assert _parse_time_str("1h 2m") == 3720

    def test_minutes_seconds_only(self) -> None:
        assert _parse_time_str("5m 30s") == 330

    def test_hours_only(self) -> None:
        assert _parse_time_str("3h") == 10800

    def test_seconds_only(self) -> None:
        assert _parse_time_str("90s") == 90

    def test_days(self) -> None:
        # 1d=86400 + 2h=7200 + 30m=1800 = 95400
        assert _parse_time_str("1d 2h 30m") == 95400

    def test_zero(self) -> None:
        assert _parse_time_str("0h 0m 0s") == 0

    def test_garbage(self) -> None:
        assert _parse_time_str("not a time") is None

    def test_empty(self) -> None:
        assert _parse_time_str("") is None


