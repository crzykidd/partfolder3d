async def _setup_admin(client: AsyncClient) -> str:
    """Setup instance and return CSRF token."""
    await client.post(
        "/api/setup",
        json={
            "admin_email": "admin@test.com",
            "admin_name": "Admin",
            "admin_password": "password1234",
        },
    )
    resp = await client.post(
        "/api/auth/login",
        json={"email": "admin@test.com", "password": "password1234"},
    )
    assert resp.status_code == 200
    return client.cookies.get("pf3d_csrf", "")


async def _create_item(
    client: AsyncClient, csrf: str, db: AsyncSession
) -> tuple[int, str]:
    """Create a library + item, return (item_id, item_key)."""
    import tempfile

    tmpdir = tempfile.mkdtemp()

    lib_resp = await client.post(
        "/api/libraries",
        json={"name": "Test Library", "mount_path": tmpdir},
        headers={"X-CSRF-Token": csrf},
    )
    assert lib_resp.status_code == 201, lib_resp.text
    lib_id = lib_resp.json()["id"]

    item_resp = await client.post(
        "/api/items",
        json={"title": "Test Widget", "library_id": lib_id},
        headers={"X-CSRF-Token": csrf},
    )
    assert item_resp.status_code == 201, item_resp.text
    data = item_resp.json()
    return data["id"], data["key"]


