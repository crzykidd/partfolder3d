@pytest.mark.asyncio
async def test_print_stats_with_data(
    client: AsyncClient, db_session: AsyncSession
) -> None:
    csrf = await _setup_admin(client)
    _, key = await _create_item(client, csrf, db_session)

    # Create 3 print records: 2 success, 1 fail
    records = [
        {"success": True, "note": "ok"},
        {"success": True, "note": "ok2"},
        {"success": False, "note": "failed"},
    ]
    for r in records:
        await client.post(
            f"/api/items/{key}/print-records",
            json=r,
            headers={"X-CSRF-Token": csrf},
        )

    resp = await client.get("/api/print-stats")
    assert resp.status_code == 200
    data = resp.json()
    assert data["total_prints"] == 3
    assert data["success_count"] == 2
    assert data["fail_count"] == 1
    assert data["success_rate"] == pytest.approx(2 / 3)
    assert len(data["most_printed_items"]) >= 1
    assert data["most_printed_items"][0]["count"] == 3


