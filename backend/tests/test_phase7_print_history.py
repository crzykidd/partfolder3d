class TestGcodeParserFile:
    """Test parse_gcode_file for binary / unknown extensions."""

    def test_bgcode_returns_empty(self, tmp_path: object) -> None:
        from pathlib import Path  # noqa: PLC0415

        from app.storage.gcode_parser import parse_gcode_file  # noqa: PLC0415

        p = Path(str(tmp_path)) / "job.bgcode"  # type: ignore[arg-type]
        p.write_bytes(b"\x00\x01\x02\x03")
        m = parse_gcode_file(p)
        assert m.filament_length_mm is None
        assert m.parse_errors == []

    def test_gcode_file_parsed(self, tmp_path: object) -> None:
        from pathlib import Path  # noqa: PLC0415

        from app.storage.gcode_parser import parse_gcode_file  # noqa: PLC0415

        content = "; filament used [mm] = 500.0\n;TIME:120\n"
        p = Path(str(tmp_path)) / "job.gcode"  # type: ignore[arg-type]
        p.write_text(content, encoding="utf-8")
        m = parse_gcode_file(p)
        assert m.filament_length_mm == pytest.approx(500.0)
        assert m.estimated_print_time_s == 120

    def test_missing_file_returns_error(self, tmp_path: object) -> None:
        from pathlib import Path  # noqa: PLC0415

        from app.storage.gcode_parser import parse_gcode_file  # noqa: PLC0415

        p = Path(str(tmp_path)) / "nonexistent.gcode"  # type: ignore[arg-type]
        m = parse_gcode_file(p)
        assert m.parse_errors != []


